_G.HumanVision = true
local hvversion = 0.1

local blockMove, blockCast
local lastMessage = 0

local function Print(message) print("<font color=\"#0000e5\"><b>Human Vision:</font> </b><font color=\"#FFFFFF\">" .. message) end

local function IsOnScreen(spot)
	local check = WorldToScreen(D3DXVECTOR3(spot.x, spot.y, spot.z))
	local x, y = check.x, check.y
	if x > 0 and x < WINDOW_W and y > 0 and y < WINDOW_H then
		return true
	end
end

_G.ValidTarget = function(object, distance, enemyTeam)
	local enemyTeam = (enemyTeam ~= false)
	return object ~= nil and object.valid and object.name and (object.type == myHero.type or object.type:find("obj_AI")) and (object.team ~= player.team) == enemyTeam and object.visible and not object.dead and (enemyTeam == false or object.bInvulnerable == 0) and (distance == nil or GetDistanceSqr(object) <= distance * distance) and IsOnScreen(object)
end

function OnIssueOrder(source, order, position, target)
	if order == 2 then
		if not IsOnScreen(position) then
			blockMove = true
		end
	elseif order == 3 then
		if not IsOnScreen(target) then
			blockMove = true
		end
	end
end

function OnCastSpell(ID, startPos, endPos, target)
	if endPos then
		if not IsOnScreen(endPos) then
			blockMove = true
		end
	elseif target then
		if not IsOnScreen(target) then
			blockCast = true
		end
	end
end

function OnSendPacket(p)
	if blockMove and p.header == 33 then
		blockMove = false
		
		p.pos = 35
		local moveid = p:Decode1()
		--print(moveid)
		if moveid == 0x37 or moveid == 0x3D then
		else
			p:Block()
		end

		if os.clock() - lastMessage > 1.5 then
			Print("Block move")
			lastMessage = os.clock()
		end
	elseif  blockCast and p.header == 313 then
		p:Block()
		blockCast = false
		if os.clock() - lastMessage > 1.5 then
			Print("Blocked cast")
			lastMessage = os.clock()
		end
	end
end

